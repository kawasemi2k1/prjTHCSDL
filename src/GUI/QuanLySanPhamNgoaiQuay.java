    private void btn_themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_themActionPerformed
        // TODO add your handling code here:
        conn = cn.getConnectDB();
        try {
            if(txt_productID.getText().equals("") || txt_ngaySX.getText().equals("") ||txt_hanSD.getText().equals("") ||txt_maCuaHang.getText().equals("") ||txt_gia.getText().equals("") ||txt_giamGia.getText().equals("") ||txt_soLuong.getText().equals(""))
                JOptionPane.showMessageDialog(this, "Bạn cần nhập đủ dữ liệu");
            else {
                StringBuffer sb = new StringBuffer();
                String sql_check_pk = "Select product_id, created_at, good_till, store_id from sales.stocks " +
                                        " where product_id = '"+txt_productID.getText()+"' and created_at = '"+txt_ngaySX.getText()+"' and good_till = '"+txt_hanSD.getText()+"' and store_id = '"+txt_maCuaHang.getText()+"'";
                Statement st = conn.createStatement();
                ResultSet rs = st.executeQuery(sql_check_pk);
                if(rs.next()){
                    sb.append("Hãy kiểm tra lại mã sản phẩm, mã cửa hàng, ngày sản xuất và hạn sử dụng");
                }
                if(sb.length() > 0){
                    JOptionPane.showMessageDialog(this, sb.toString());
                } else {
                    String sql = "Insert into sales.stocks values("+txt_productID.getText()+", '"+txt_ngaySX.getText()+"', '"+
                            txt_hanSD.getText()+"', "+txt_maCuaHang.getText()+", "+txt_gia.getText()+", "+
                            txt_giamGia.getText()+", "+txt_soLuong.getText()+");" ;
                    System.out.println(txt_ngaySX.getText());
                    st = conn.createStatement();
                    int kq = st.executeUpdate(sql);
                    if(kq > 0) {
                        JOptionPane.showMessageDialog(this, "Thêm mới thành công");
                        Reset();
                    }
                }
                st.close();
            }
            conn.close();
            rs.close();
           
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btn_themActionPerformed

