    public void loadBang(){
        try {
            conn = cn.getConnectDB();
            int number;
            Vector row;
            String sql = "select * from sales.stocks";
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            ResultSetMetaData metaData = rs.getMetaData();
            number = metaData.getColumnCount();
            tb.setRowCount(0);
             while (rs.next()) {
                row = new Vector();
                for (int i = 1; i <= number; i++) {
                    row.addElement(rs.getString(i));
                }
                tb.addRow(row);
                tbl_sanPhamBan.setModel(tb);
            }
             
            st.close();
            rs.close();
            conn.close();
        } catch (Exception e) {
        }
        tbl_sanPhamBan.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent lse) {
                if(tbl_sanPhamBan.getSelectedRow() >= 0){
                    txt_productID.setText(tbl_sanPhamBan.getValueAt(tbl_sanPhamBan.getSelectedRow(), 0)+ "");
                    txt_ngaySX.setText(tbl_sanPhamBan.getValueAt(tbl_sanPhamBan.getSelectedRow(), 1)+ "");
                    txt_hanSD.setText(tbl_sanPhamBan.getValueAt(tbl_sanPhamBan.getSelectedRow(), 2)+ "");
                    txt_maCuaHang.setText(tbl_sanPhamBan.getValueAt(tbl_sanPhamBan.getSelectedRow(), 3)+ "");
                    txt_gia.setText(tbl_sanPhamBan.getValueAt(tbl_sanPhamBan.getSelectedRow(), 4)+ "");
                    txt_giamGia.setText(tbl_sanPhamBan.getValueAt(tbl_sanPhamBan.getSelectedRow(), 5)+ "");
                    txt_soLuong.setText(tbl_sanPhamBan.getValueAt(tbl_sanPhamBan.getSelectedRow(), 6)+ "");   
                }
            }
        });
    }
